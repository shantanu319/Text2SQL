```sql
SELECT 
    (SUM(CASE WHEN YEAR = 2021 THEN COUNT(*) ELSE 0 END) - SUM(CASE WHEN YEAR = 2011 THEN COUNT(*) ELSE 0 END)) * 100 / SUM(CASE WHEN YEAR IN (2021, 2011) THEN COUNT(*) ELSE 0 END)
FROM 
    accident_data
WHERE 
    PRIMARY_COLLISION_FACTOR_VIOLATION_CATEGORY = 'Most Common Cause'
GROUP BY 
    PRIMARY_COLLISION_FACTOR_VIOLATION_CATEGORY;
```