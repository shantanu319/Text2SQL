```sql
SELECT 
    YEAR,
    CATEGORY,
    AVG(WHOLESALE_PRICE) AS AVERAGE_WHOLESALE_PRICE,
    MAX(WHOLESALE_PRICE) AS MAXIMUM_WHOLESALE_PRICE,
    MIN(WHOLESALE_PRICE) AS MINIMUM_WHOLESALE_PRICE,
    WHOLESALE_PRICE - AVG(WHOLESALE_PRICE) AS WHOLESALE_PRICE_DIFFERENCE,
    SUM(WHOLESALE_PRICE) AS TOTAL_WHOLESALE_PRICE,
    SUM(SELLING_PRICE) AS TOTAL_SELLING_PRICE,
    (SUM(WHOLESALE_PRICE) - SUM(SELLING_PRICE)) / SUM(WHOLESALE_PRICE) * 100 AS AVERAGE_LOSS_RATE,
    SUM(WHOLESALE_PRICE) - SUM(SELLING_PRICE) AS TOTAL_LOSS,
    SUM(SELLING_PRICE) - SUM(WHOLESALE_PRICE) AS PROFIT
FROM 
    VEG_WHSLE_DATA
WHERE 
    YEAR BETWEEN 2020 AND 2023
GROUP BY 
    YEAR, CATEGORY
ORDER BY 
    YEAR, CATEGORY;
